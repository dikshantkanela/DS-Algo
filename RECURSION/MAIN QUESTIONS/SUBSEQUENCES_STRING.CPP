#include <iostream>
#include <vector>
using namespace std;

// âœ… Recursive function to generate all subsequences of a string
void solve(string &str, string output, vector<string> &ans, int index) {
    // ğŸ›‘ Base Case: Reached the end of the string
    if (index > str.length() - 1) {
        if (output != "") {         // âœ‚ï¸ Skip empty string
            ans.push_back(output);  // âœ… Add non-empty subsequence
        }
        return;
    }

    // ğŸ”´ Exclude the current character
    solve(str, output, ans, index + 1);

    // ğŸŸ¢ Include the current character
    char element = str[index];
    output.push_back(element);  // or use output += element;
    solve(str, output, ans, index + 1);
}

// ğŸ” Function to generate all non-empty subsequences of a string
vector<string> subsequences(string str) {
    vector<string> ans;
    string output = "";
    int index = 0;
    solve(str, output, ans, index);
    return ans;
}

// ğŸ§ª Main function to test the above logic
int main() {
    string input = "abc";
    vector<string> result = subsequences(input);

    // ğŸ–¨ï¸ Output the result
    cout << "All non-empty subsequences of \"" << input << "\":" << endl;
    for (const string &s : result) {
        cout << s << endl;
    }

    return 0;
}
